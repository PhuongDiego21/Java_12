    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        // TODO add your handling code here:
        if (Tablekhachhang.getSelectedRow() == -1) {
            if (Tablekhachhang.getRowCount() == 0) {
               lbkhachhang.setText("Table is empty");
            } else {
                 lbkhachhang.setText("You must choose a service");
            }
        } else {
            DefaultTableModel model = (DefaultTableModel) Tablekhachhang.getModel();
            model.setValueAt(txtmakh.getText(), Tablekhachhang.getSelectedRow(), 0);
            model.setValueAt(txttenkh.getText(), Tablekhachhang.getSelectedRow(), 1);
            model.setValueAt(txtcmt.getText(), Tablekhachhang.getSelectedRow(), 2);
            model.setValueAt(txtsdt.getText(), Tablekhachhang.getSelectedRow(), 3);
            model.setValueAt(txtnamsinh.getText(), Tablekhachhang.getSelectedRow(), 4);
            model.setValueAt(txtdiachi.getText(), Tablekhachhang.getSelectedRow(), 5);
        }
    }//GEN-LAST:event_btnsuaActionPerformed

