    public ArrayList<khachhang> layDanhSachKhachhang() {
        ArrayList<khachhang> dskh = new ArrayList<>();
        Connection con = getConnection();
        try {
            st = (Statement) con.createStatement();
            String sql = "SELECT * FROM khachhang";
            // Thực thi câu lệnh truy vấn
            ResultSet rs = st.executeQuery(sql);

            khachhang kh;
            while (rs.next()) {
                kh= new khachhang(rs.getString("Makh"), rs.getString("Tenkh"),rs.getString("CMT"), rs.getString("SDT"), rs.getInt("Namsinh"), rs.getString("Diachi"));

                //Thêm vào danh sách
                dskh.add(kh);
            }
        } catch (SQLException ex) {
        }
        return dskh;
    }

    public void hienThiDanhSachKhachhang() {
        String colTieuDe1[] = new String[]{"Mã khách hàng", "Tên khách hàng", "CMT","SDT", "Năm sinh","Địa chỉ"};
        ArrayList<khachhang> dskh = layDanhSachKhachhang();

        DefaultTableModel model = new DefaultTableModel(colTieuDe1, 0);

        Object[] row;

        for (int i = 0; i < dskh.size(); i++) {

            row = new Object[6];

            // GÃ�N GIÃ� TRá»Š
            row[0] = dskh.get(i).getMakh();
            row[1] = dskh.get(i).getTenkh();
            row[2] = dskh.get(i).getCMT();
            row[3] = dskh.get(i).getSDT();
            row[4] = dskh.get(i).getNamsinh();
            row[5] = dskh.get(i).getDiachi();

            model.addRow(row);
        }
        Tablekhachhang.setModel(model);

    }
