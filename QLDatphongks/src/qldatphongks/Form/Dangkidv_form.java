    public ArrayList<Dangkidv> layDanhSachDangkidv() {
        ArrayList<Dangkidv> dsdkdv = new ArrayList<>();
        Connection con = getConnection();
        try {
            st = (Statement) con.createStatement();
            String sql = "SELECT * FROM Dangkidv";
            // Thực thi câu lệnh truy vấn
            ResultSet rs = st.executeQuery(sql);

            Dangkidv dkdv;
            while (rs.next()) {
                dkdv = new Dangkidv(rs.getString("Madv"), rs.getString("Madp"), rs.getString("Tendv"),rs.getDouble("Giadv"));

                //Thêm vào danh sách
                dsdkdv.add(dkdv);
            }
        } catch (SQLException ex) {
        }
        return dsdkdv;
    }

    public void hienThiDanhSachDangkidv() {
        String colTieuDe1[] = new String[]{"Mã dịch vụ","Mã đặt phòng" ,"Tên dịch vụ", "Giá dịch vụ"};
        ArrayList<Dangkidv> dsdkdv = layDanhSachDangkidv();

        DefaultTableModel model = new DefaultTableModel(colTieuDe1, 0);

        Object[] row;

        for (int i = 0; i < dsdkdv.size(); i++) {

            row = new Object[4];

            // GÃ�N GIÃ� TRá»Š
            row[0] = dsdkdv.get(i).getMadv();
            row[1] = dsdkdv.get(i).getMadp();
            row[2] = dsdkdv.get(i).getTendv();
            row[3] = dsdkdv.get(i).getGiadv();

            model.addRow(row);
        }
        Tabledangkidv.setModel(model);

    }
    
