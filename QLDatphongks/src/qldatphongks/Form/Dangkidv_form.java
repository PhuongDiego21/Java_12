    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        // TODO add your handling code here:
        if (Tabledangkidv.getSelectedRow() == -1) {
            if (Tabledangkidv.getRowCount() == 0) {
               lbdangkidv.setText("Table is empty");
            } else {
                 lbdangkidv.setText("You must choose a service");
            }
        } else {
            DefaultTableModel model = (DefaultTableModel) Tabledangkidv.getModel();
            model.setValueAt(txtmadv.getText(), Tabledangkidv.getSelectedRow(), 0);
            model.setValueAt(txtmadp.getText(), Tabledangkidv.getSelectedRow(), 1);
            model.setValueAt(txttendv.getText(), Tabledangkidv.getSelectedRow(), 2);
            model.setValueAt(txtgiadv.getText(), Tabledangkidv.getSelectedRow(), 3);
            
        }
    }//GEN-LAST:event_btnsuaActionPerformed

