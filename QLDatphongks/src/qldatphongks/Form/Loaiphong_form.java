    private void btnsualphActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsualphActionPerformed
        // TODO add your handling code here:
        if (Tableloaiphong.getSelectedRow() == -1) {
            if (Tableloaiphong.getRowCount() == 0) {
               lbloaiphong.setText("Table is empty");
            } else {
                 lbloaiphong.setText("You must choose a service");
            }
        } else {
            DefaultTableModel model = (DefaultTableModel) Tableloaiphong.getModel();
            model.setValueAt(txtmaloai.getText(), Tableloaiphong.getSelectedRow(), 0);
            model.setValueAt(txttenloai.getText(), Tableloaiphong.getSelectedRow(), 1);
            model.setValueAt(txtgialoai.getText(), Tableloaiphong.getSelectedRow(), 2);
            
        }
    }//GEN-LAST:event_btnsualphActionPerformed

