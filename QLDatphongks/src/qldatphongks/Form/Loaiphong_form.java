    public ArrayList<Loaiphong> layDanhSachLoaiphong() {
        ArrayList<Loaiphong> dslp = new ArrayList<>();
        Connection con = getConnection();
        try {
            st = (Statement) con.createStatement();
            String sql = "SELECT * FROM Loaiphong";
            // Thực thi câu lệnh truy vấn
            ResultSet rs = st.executeQuery(sql);

            Loaiphong lp;
            while (rs.next()) {
                lp = new Loaiphong(rs.getString("Maloai"), rs.getString("Tenloai"), rs.getDouble("Gialoai"));

                //Thêm vào danh sách
                dslp.add(lp);
            }
        } catch (SQLException ex) {
        }
        return dslp;
    }

    public void hienThiDanhSachLoaiphong() {
        String colTieuDe1[] = new String[]{"Mã loại phòng", "Tên loại phòng", "Giá loại phòng"};
        ArrayList<Loaiphong> dslp = layDanhSachLoaiphong();

        DefaultTableModel model = new DefaultTableModel(colTieuDe1, 0);

        Object[] row;

        for (int i = 0; i < dslp.size(); i++) {

            row = new Object[3];

            // GÃ�N GIÃ� TRá»Š
            row[0] = dslp.get(i).getMaloai();
            row[1] = dslp.get(i).getTenloai();
            row[2] = dslp.get(i).getGialoai();

            model.addRow(row);
        }
        Tableloaiphong.setModel(model);

    }


