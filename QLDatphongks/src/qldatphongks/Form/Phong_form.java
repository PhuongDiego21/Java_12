    private void btnthemphActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemphActionPerformed
        // TODO add your handling code here:
        Connection con = getConnection();
        try {
          
            st = (Statement) con.createStatement();
            String query = "INSERT INTO Phong(Maph,Tenph,Maloai, Giaph) VALUES('" + txtmaph.getText() + "',"
                    + "'" + txttenph.getText() + "','"+txtmaloai.getText()+"','" + txtgiaph.getText() + "')";

            st.execute(query);
            hienThiDanhSachPhong();

        } catch (SQLException ex) {
        }
    }//GEN-LAST:event_btnthemphActionPerformed

    private void btnsuaphActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaphActionPerformed
        // TODO add your handling code here:
        if (tablephong.getSelectedRow() == -1) {
            if (tablephong.getRowCount() == 0) {
               lbphong.setText("Table is empty");
            } else {
                 lbphong.setText("You must choose a service");
            }
        } else {
            DefaultTableModel model = (DefaultTableModel) tablephong.getModel();
            model.setValueAt(txtmaph.getText(), tablephong.getSelectedRow(), 0);
            model.setValueAt(txttenph.getText(), tablephong.getSelectedRow(), 1);
            model.setValueAt(txtmaloai.getText(), tablephong.getSelectedRow(), 2);
            model.setValueAt(txtgiaph.getText(), tablephong.getSelectedRow(), 3);
            
        }
               
    }//GEN-LAST:event_btnsuaphActionPerformed

