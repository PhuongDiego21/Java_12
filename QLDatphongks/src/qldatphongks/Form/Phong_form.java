    public ArrayList<Phong> layDanhSachPhong() {
        ArrayList<Phong> dsph = new ArrayList<>();
        Connection con = getConnection();
        try {
            st = (Statement) con.createStatement();
            String sql = "SELECT * FROM Phong";
            // Thực thi câu lệnh truy vấn
            ResultSet rs = st.executeQuery(sql);

            Phong ph;
            while (rs.next()) {
                ph = new Phong(rs.getString("Maph"), rs.getString("Tenph"),rs.getString("Maloai"), rs.getDouble("Giaph"));

                //Thêm vào danh sách
                dsph.add(ph);
            }
        } catch (SQLException ex) {
        }
        return dsph;
    }

    public void hienThiDanhSachPhong() {
        String colTieuDe1[] = new String[]{"Mã phòng", "Tên phòng","Mã loại", "Giá phòng"};
        ArrayList<Phong> dsph = layDanhSachPhong();

        DefaultTableModel model = new DefaultTableModel(colTieuDe1, 0);

        Object[] row;

        for (int i = 0; i < dsph.size(); i++) {

            row = new Object[4];

            // GÃ�N GIÃ� TRá»Š
            row[0] = dsph.get(i).getMaph();
            row[1] = dsph.get(i).getTenph();
            row[2] = dsph.get(i).getMaloai();
            row[3]= dsph.get(i).getGiaph();

            model.addRow(row);
        }
        tablephong.setModel(model);

    }


